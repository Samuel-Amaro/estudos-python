numero = int(input('Digite um Valor: '))

dobro = numero * numero
triplo = numero * numero * numero
raiz = numero ** (1/2) # representando raiz quadrada atraves da exponecianção
     
print('Numero Digitado Foi: {}\n Dobro Do Numero: {}\n Triplo do Numero: {}\n Raiz-Quadrada do Numero: {}'.format(numero,dobro,triplo,raiz))